static int display_vga_probe(struct ns115_display_device *device, void *devdata)
{
       DBG("%s start", __FUNCTION__);
       
	device->owner = THIS_MODULE;
	strcpy(device->type, "vga");
	device->name = "vga";
	device->priority = DISPLAY_PRIORITY_VGA;
	//device->priority = DISPLAY_PRIORITY_TV;
	device->priv_data = devdata;
	device->ops = &vga_display_ops;
	return 1;
}

static struct ns115_display_driver display_vga = {
	.probe = display_vga_probe,
};

int cs8556_register_display_vga(struct device *parent)
{
	int i;

       DBG("%s start", __FUNCTION__);
	
	memset(&vga_monspecs, 0, sizeof(struct cs8556_monspecs));
    
	INIT_LIST_HEAD(&vga_monspecs.modelist);
	for(i = 0; i < ARRAY_SIZE(vga_mode); i++)
		fb_add_videomode(&vga_mode[i], &vga_monspecs.modelist);
		
	JCG("---mode:%d---",cs8556.mode);
    if(cs8556.mode > TVOUT_CVBS_PAL) 
	{
		JCG();
		vga_monspecs.mode = (struct fb_videomode *)&(vga_mode[cs8556.mode - 3]);
		vga_monspecs.mode_set = cs8556.mode;
	}
	else
	{
		JCG();
		vga_monspecs.mode = (struct fb_videomode *)&(vga_mode[1]);
		vga_monspecs.mode_set = TVOUT_VGA_800x600p_60;
	}

	vga_monspecs.ddev = ns115_display_device_register(&display_vga, parent, &cs8556);

	cs8556.vga = &vga_monspecs;
    
    return 0;
}


int cs8556_unregister_display_vga(struct ns115_display_device *device)
{
	ns115_display_device_unregister(device);
}



